var time = 13;

if (time >= 9 && time < 10) {
	console.log("Wake Up and Read a book!");
} else if (time >= 10 && time < 11) {
	console.log("Have Breakfast");
} else if (time >= 11 && time < 14) {
	console.log("Do Homework!");
} else if (time >= 14 && time < 15) {
	console.log("Have Lunch!");
} else if (time >= 15 && time < 17) {
	console.log("Rest Time!");
} else if (time >= 17 && time < 19) {
	console.log("Front-end Courses!");
} else if (time >= 19 && time < 20) {
	console.log("Have Dinner!");
} else if (time >= 20 && time < 21) {
	console.log("Work Out!");
} else if (time >= 21 && time < 23) {
	console.log("Shower and Rest!");
} else if (time >= 23 && time <= 24) {
	console.log("Sleep");
} else if (time >= 0 && time < 9) {
	console.log("Sleep");
} else {
	console.log("Wrong Time!");
}

